public class Kuis_1_Lana {
    public static void main(String[] args) {
        int x = 0;
        while (x <= 4) {
            int y = 1;
            while (y <= x) {
                System.out.print(y + " ");
                y++;
            }
            x++;
            System.out.print("\n");
        }
    }
}

