public class ContohVariabel {
    public static void main(String[] args) {
        String hobiSaya = "Membaca buku pemrograman";
        boolean mahasiswaPandai = true;
        char jenisKelamin = 'L';
        byte umurSaya = 19;
        double ipkSaya = 3.85, tinggi = 1.72;
        System.out.println(hobiSaya);
        System.out.println("Apakah kamu pandai? " + mahasiswaPandai);
        System.out.println("Jenis kelamin: " + jenisKelamin);
        System.out.println("Umurku saat ini: " + umurSaya);
        System.out.printf("Saya ber-ipk %s, dengan tinggi badan %s centimeter%n", ipkSaya, tinggi);
    }
}
