public class Persegi {
    public static void main(String[] args) {
        int L = luasPersegi(5);
        System.out.println("Luas persegi dengan sisi 5 adalah " + L);

    }
    static int luasPersegi(int sisi) {
        int luas = sisi * sisi;
        return luas;
    }
}
